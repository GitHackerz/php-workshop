<?php

// Indexed Array
$colors = ["red", "green", "blue"];
echo $colors[1];  // Output: green

// Associative Array
$person = ["name" => "John", "age" => 25];
echo $person["name"];  // Output: John

// Multidimensional Array
$matrix = [
    [1, 2, 3],
    [4, 5, 6]
];
echo $matrix[1][2];  // Output: 6

?>
